#Arithmetic
x = 10
y = 4

print('x = 10, y = 4')
print('x + y =',x+y)
# Output: x + y = 14
 
print('x - y =',x-y)
# Output: x - y = 6

print('x * y =',x*y)
# Output: x * y = 40

print('x / y =',x/y)
# Output: x / y = 2.50

print('x // y =',x//y)
# Output: x // y = 2

print('x ** y =',x**y)
# Output: x ** y = 10000

#Comparison
x = input('Enter First Number: ')
y = input('Enter Second Number: ')
print(x, y)
print('x > y  is',x>y)
# Output: x > y is False

print('x < y  is',x<y)
# Output: x < y is True

print('x == y is',x==y)
# Output: x == y is False

print('x != y is',x!=y)
# Output: x != y is True

print('x >= y is',x>=y)
# Output: x >= y is False

print('x <= y is',x<=y)
# Output: x <= y is True

#Logical
a = True
b = False

print('a and b is',a and b)
# Output: a and b is False

print('a or b is',a or b)
# Output: a or b is True

print('not a is',not a)
# Output: not a is False

